    function oneEditAway($first, $second)
    {
        if ($first === $second) {
            return true;
        }
        $strlenFirst = strlen($first);
        $strlenSecond = strlen($second);
        $result = false;
        if ($strlenFirst === $strlenSecond) {
            for ($i = 0; $i < $strlenFirst; $i++) {
                if ($first[$i] !== $second[$i]) {
                    if ($result === false) {
                        $result = true;
                    } else {
                        $result = false;
                        break;
                    }
                }
            }
        } elseif ($strlenFirst === $strlenSecond + 1) {
            for ($i = 0; $i < $strlenFirst; $i++) {
                if (substr($first, 0, $i) . substr($first, $i + 1) === $second) {
                    $result = true;
                    break;
                }
            }
        } elseif ($strlenSecond === $strlenFirst + 1) {
            for ($i = 0; $i < $strlenSecond; $i++) {
                if (substr($second, 0, $i) . substr($second, $i + 1) === $first) {
                    $result = true;
                    break;
                }
            }
        }

        return $result;
    }

